void main() {
//*comparison_operators
/*
*1_ (>) Greater than:- Used to check that the first value is greater than the second value :
أكبر من :- تستخدم للتحقق من أن القيمه ألأولي أكبر من القيمه الثانية
*2_ (>=) Greater than or equal to:- Used to check that the first value is greater than or equal to the second value :
أكبر من أو يساوي :- تستخدم للتحقق من أن القيمه ألأولي أكبر من القيمه الثانية أو تساويها
*3_ (<)  Less than:- Used to check that the first value is less than the second value :

أصغر من :- تستخدم للتحقق من أن القيمه ألأولي أًصغر من القيمه الثانية
*4_ (<=) Less than or equal to:- Used to check that the first value is less than or equal to the second value :
أصغر من أو يساوي :- تستخدم للتحقق من أن القيمه ألأولي أصغر من القيمه الثانية أو تساويها
*5_ (==) Equal to:- Used to check that the first value is equal to the second value :
يساوي :- تستخدم للتحقق من أن القيمه ألأولي تساوي القيمه الثانية
*6_ (!=) Not equal to:- Used to check that the first value is not equal to the second value :
لا يساوي :- تستخدم للتحقق من أن القيمه ألأولي لا تساوي القيمه الثانية
 */
  num firstValue = 15;
  num secondValue = 20;
  bool comparsionResult = false;
//*1_ (>)
  comparsionResult = firstValue > secondValue;
  print(comparsionResult);
//*2_ (>=)
  comparsionResult = firstValue >= secondValue;
  print(comparsionResult);
//*3_ (<)
  comparsionResult = firstValue < secondValue;
  print(comparsionResult);
//*4_ (<=)
  comparsionResult = firstValue <= secondValue;
  print(comparsionResult);
//*5_ (==)
  comparsionResult = firstValue == secondValue;
  print(comparsionResult);
//*6_ (!)

  comparsionResult = firstValue != secondValue;

  print(comparsionResult);

}
